#ifndef ZEPHYR_DRIVERS_SENSOR_MAX17201_USER_H_
#define ZEPHYR_DRIVERS_SENSOR_MAX17201_USER_H_

#include <zephyr/drivers/fuel_gauge.h>

/* STATUS REGISTER FLAGS */
#define MAX1720X_FLAGS_ALERT_CURR_MIN    (0x0001U << 2)
#define MAX1720X_FLAGS_ALERT_CURR_MAX    (0x0001U << 6)
#define MAX1720X_FLAGS_ALERT_SOC_PERCENT (0x0001U << 7)
#define MAX1720X_FLAGS_ALERT_VOLT_MIN    (0x0001U << 8)
#define MAX1720X_FLAGS_ALERT_TEMP_MIN    (0x0001U << 9)
#define MAX1720X_FLAGS_ALERT_SOC_MIN     (0x0001U << 10)
#define MAX1720X_FLAGS_ALERT_BATTERY_IN  (0x0001U << 11)
#define MAX1720X_FLAGS_ALERT_VOLT_MAX    (0x0001U << 12)
#define MAX1720X_FLAGS_ALERT_TEMP_MAX    (0x0001U << 13)
#define MAX1720X_FLAGS_ALERT_SOC_MAX     (0x0001U << 14)
#define MAX1720X_FLAGS_ALERT_BATTERY_OUT (0x0001U << 15)

/* USER INTERRUPT MASK */
#define MAX1720X_ALERT_ENABLE_THRESHOLDS  ((0x0001U << (16 + 6)) | (0x0001U << 2))
#define MAX1720X_ALERT_ENABLE_SOC_PERCENT ((0x0001U << (16 + 7)) | (0x0001U << (16 + 6)))

/* MAX1720X Specific Properties */
enum max1720x_fuel_gauge_prop_type {
	MAX1720X_FUEL_GAUGE_ALERT_ENABLE = FUEL_GAUGE_CUSTOM_BEGIN + 0,
	MAX1720X_FUEL_GAUGE_SOC_THRESHOLDS = FUEL_GAUGE_CUSTOM_BEGIN + 1,
	MAX1720X_FUEL_GAUGE_CURR_THRESHOLDS = FUEL_GAUGE_CUSTOM_BEGIN + 2,
	MAX1720X_FUEL_GAUGE_VOLT_THRESHOLDS = FUEL_GAUGE_CUSTOM_BEGIN + 3,
	MAX1720X_FUEL_GAUGE_TEMP_THRESHOLDS = FUEL_GAUGE_CUSTOM_BEGIN + 4
};

#endif /* ZEPHYR_DRIVERS_SENSOR_MAX17201_USER_H_ */
